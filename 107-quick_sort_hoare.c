/**
 * swap - Swap two.
 * @a:  first .
 * @b:  second.
 */
void swap(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

/**
 * hoare_partition - hoare partition scheme order a subse array.
 * @array: The array of integers.
 * @size: array size.
 * @left: starting index.
 * @right: ending index.
 * Return:  partition index.
 * Description:  swap of two elements by pivot element.
 */
int hoare_partition(int *array, size_t size, int left, int right)
{
	int pivot, above, below;

	pivot = array[right];
	for (above = left - 1, below = right + 1; above < below;)
	{
		do {
			above++;
		} while (array[above] < pivot);
		do {
			below--;
		} while (array[below] > pivot);

		if (above < below)
		{
			swap(array + above, array + below);
			print_array(array, size);
		}
	}

	return (above);
}

/**
 * hoare_sort - quicksort algorithm using recursion.
 * @array: array of integers.
 * @size: array size.
 * @left: starting index
 * @right: ending index.
 * Description: Hoare partition scheme.
 */
void hoare_sort(int *array, size_t size, int left, int right)
{
	int part;

	if (right - left > 0)
	{
		part = hoare_partition(array, size, left, right);
		hoare_sort(array, size, left, part - 1);
		hoare_sort(array, size, part, right);
	}
}

/**
 * quick_sort_hoare -  quicksort algorithm for array in asc
 * @array: array.
 * @size: array size.
 * Description: Hoare partition scheme.
 */
void quick_sort_hoare(int *array, size_t size)
{
	if (array == NULL || size < 2)
		return;

	hoare_sort(array, size, 0, size - 1);
}
